DROP DATABASE IF EXISTS WEEK12;CREATE DATABASE WEEK12;USE WEEK12;

CREATE TABLE CITIES(CITY CHAR(20) UNIQUE NOT NULL,STATE CHAR(20));

CREATE TABLE WAREHOUSES(WID INTEGER PRIMARY KEY AUTO_INCREMENT,WNAME CHAR(30),LOCATION CHAR(20),EXTRA_CONTEXT json, FOREIGN KEY(LOCATION) REFERENCES CITIES(CITY) )AUTO_INCREMENT = 300;

CREATE TABLE STORES(SID INTEGER PRIMARY KEY AUTO_INCREMENT,STORE_NAME CHAR(20), LOCATION_CITY CHAR(20) , WID INT NOT NULL, FOREIGN KEY(WID) REFERENCES WAREHOUSES(WID), FOREIGN KEY(LOCATION_CITY) REFERENCES CITIES(CITY) )AUTO_INCREMENT = 400;

CREATE TABLE CUSTOMER(CNO INTEGER PRIMARY KEY AUTO_INCREMENT, CNAME CHAR(50),ADDR VARCHAR(50),CU_CITY CHAR(20), FOREIGN KEY(CU_CITY) REFERENCES CITIES(CITY))AUTO_INCREMENT = 500;

CREATE TABLE ORDERS(ONO INTEGER PRIMARY KEY AUTO_INCREMENT,ODATE DATE, CNO INTEGER, ITEMNO INTEGER, FOREIGN KEY(CNO) REFERENCES CUSTOMER(CNO), FOREIGN KEY(ITEMNO) REFERENCES ITEMS(ITEMNO))AUTO_INCREMENT = 600;

CREATE TABLE ITEMS(ITEMNO INTEGER PRIMARY KEY AUTO_INCREMENT,DESCRIPTION TEXT,WEIGHT DECIMAL(5,2),COST DECIMAL(10,2) , CNO INT NOT NULL, FOREIGN KEY(CNO) REFERENCES CUSTOMER(CNO))AUTO_INCREMENT = 700;




